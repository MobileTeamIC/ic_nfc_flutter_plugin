import 'enum_nfc.dart';
import 'nfc_config.dart';

/// Predefined configurations for common NFC use cases
class NfcPresets {
  /// QR -> NFC flow (no manual inputs needed)
  static NfcConfig qrToNfc({
    NfcLanguage languageSdk = NfcLanguage.icnfc_vi,
    bool isShowTutorial = true,
    bool isEnableGotIt = true,
    bool isEnableUploadImage = true,
    bool isEnablePostcodeMatching = false,
    String inputClientSession = '',
    List<String> readingTagsNFC = const [],
    String nameVideoHelpNFC = '',
    String accessToken = '',
    String tokenId = '',
    String tokenKey = '',
    String accessTokenEKYC = '',
    String tokenIdEKYC = '',
    String tokenKeyEKYC = '',
    String baseUrl = '',

  }) => NfcConfig(

    accessToken: accessToken,
    tokenId: tokenId,
    tokenKey: tokenKey,
    accessTokenEKYC: accessTokenEKYC,
    tokenIdEKYC: tokenIdEKYC,
    tokenKeyEKYC: tokenKeyEKYC,
    baseUrl: baseUrl,
    languageSdk: languageSdk,
    isShowTutorial: isShowTutorial,
    isEnableGotIt: isEnableGotIt,
    readerCardMode: ReaderCardMode.qrCode,
    isEnableUploadImage: isEnableUploadImage,
    isEnablePostcodeMatching: isEnablePostcodeMatching,
    inputClientSession: inputClientSession,
    readingTagsNFC: readingTagsNFC,
    nameVideoHelpNFC: nameVideoHelpNFC,

  );

  /// MRZ -> NFC flow (no manual inputs needed)
  static NfcConfig mrzToNfc({
    String accessToken = '',
    String tokenId = '',
    String tokenKey = '',
    String accessTokenEKYC = '',
    String tokenIdEKYC = '',
    String tokenKeyEKYC = '',
    String baseUrl = '',
    NfcLanguage languageSdk = NfcLanguage.icnfc_vi,
    bool isShowTutorial = true,
    bool isEnableGotIt = true,
    bool isEnableUploadImage = true,
    bool isEnablePostcodeMatching = false,
    String inputClientSession = '',
    List<String> readingTagsNFC = const [],
    String nameVideoHelpNFC = '',
  }) => NfcConfig(
    accessToken: accessToken,
    tokenId: tokenId,
    tokenKey: tokenKey,
    accessTokenEKYC: accessTokenEKYC,
    tokenIdEKYC: tokenIdEKYC,
    tokenKeyEKYC: tokenKeyEKYC,
    baseUrl: baseUrl,
    languageSdk: languageSdk,
    isShowTutorial: isShowTutorial,
    isEnableGotIt: isEnableGotIt,
    readerCardMode: ReaderCardMode.mrzCode,
    isEnableUploadImage: isEnableUploadImage,
    isEnablePostcodeMatching: isEnablePostcodeMatching,
    inputClientSession: inputClientSession,
    readingTagsNFC: readingTagsNFC,
    nameVideoHelpNFC: nameVideoHelpNFC,
  );

  /// Manual NFC with SDK UI
  static NfcConfig manualWithUi({
    String accessToken = '',
    String tokenId = '',
    String tokenKey = '',
    String accessTokenEKYC = '',
    String tokenIdEKYC = '',
    String tokenKeyEKYC = '',
    String baseUrl = '',
    required String idNumber,
    required String birthday,
    required String expiredDate,
    NfcLanguage languageSdk = NfcLanguage.icnfc_vi,
    bool isShowTutorial = true,
    bool isEnableGotIt = true,
    bool isEnableUploadImage = true,
    bool isEnablePostcodeMatching = false,
    String inputClientSession = '',
    List<String> readingTagsNFC = const [],
    String nameVideoHelpNFC = '',
  }) => NfcConfig(
    accessToken: accessToken,
    tokenId: tokenId,
    tokenKey: tokenKey,
    accessTokenEKYC: accessTokenEKYC,
    tokenIdEKYC: tokenIdEKYC,
    tokenKeyEKYC: tokenKeyEKYC,
    baseUrl: baseUrl,
    idNumber: idNumber,
    birthday: birthday,
    expiredDate: expiredDate,
    languageSdk: languageSdk,
    isShowTutorial: isShowTutorial,
    isEnableGotIt: isEnableGotIt,
    readerCardMode: ReaderCardMode.nfcReader,
    isEnableUploadImage: isEnableUploadImage,
    isEnablePostcodeMatching: isEnablePostcodeMatching,
    inputClientSession: inputClientSession,
    readingTagsNFC: readingTagsNFC,
    nameVideoHelpNFC: nameVideoHelpNFC,
  );

  /// Manual NFC without SDK UI
  static NfcConfig manualWithoutUi({
    required String idNumber,
    required String birthday,
    required String expiredDate,
    String accessToken = '',
    String tokenId = '',
    String tokenKey = '',
    String accessTokenEKYC = '',
    String tokenIdEKYC = '',
    String tokenKeyEKYC = '',
    String baseUrl = '',
    NfcLanguage languageSdk = NfcLanguage.icnfc_vi,
    bool isEnableUploadImage = true,
    bool isEnablePostcodeMatching = false,
    String inputClientSession = '',
    List<String> readingTagsNFC = const [],
    String nameVideoHelpNFC = '',
  }) => NfcConfig(
    accessToken: accessToken,
    tokenId: tokenId,
    tokenKey: tokenKey,
    accessTokenEKYC: accessTokenEKYC,
    tokenIdEKYC: tokenIdEKYC,
    tokenKeyEKYC: tokenKeyEKYC,
    baseUrl: baseUrl,
    idNumber: idNumber,
    birthday: birthday,
    expiredDate: expiredDate,
    readerCardMode: ReaderCardMode.nfcOutside,
    isEnableUploadImage: isEnableUploadImage,
    isEnablePostcodeMatching: isEnablePostcodeMatching,
    inputClientSession: inputClientSession,
    readingTagsNFC: readingTagsNFC,
    nameVideoHelpNFC: nameVideoHelpNFC,
  );
}
